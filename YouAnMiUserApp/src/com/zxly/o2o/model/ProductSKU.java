package com.zxly.o2o.model;

public class ProductSKU {

	private String paramComIds;
	private float price;
	private int quantity;

	public String getParamComIds() {
		return paramComIds;
	}

	public void setParamComIds(String paramComIds) {
		this.paramComIds = paramComIds;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

}
