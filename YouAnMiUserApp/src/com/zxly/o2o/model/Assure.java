package com.zxly.o2o.model;

public class Assure {
	private String scopeNames;
	private String assureDays;
	private float assureRate;
	public String getScopeNames() {
		return scopeNames;
	}
	public void setScopeNames(String scopeNames) {
		this.scopeNames = scopeNames;
	}
	public String getAssureDays() {
		return assureDays;
	}
	public void setAssureDays(String assureDays) {
		this.assureDays = assureDays;
	}
	public float getAssureRate() {
		return assureRate;
	}
	public void setAssureRate(float assureRate) {
		this.assureRate = assureRate;
	}
	
}
