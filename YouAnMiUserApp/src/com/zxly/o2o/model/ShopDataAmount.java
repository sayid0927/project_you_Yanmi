package com.zxly.o2o.model;

public class ShopDataAmount {
	private int productAmount;
	private int newProductAmount;
	private int activityAmount;
	private long visitAmount;
	private int collectAmoun;
	private int topicAmount;

	public int getProductAmount() {
		return productAmount;
	}

	public void setProductAmount(int productAmount) {
		this.productAmount = productAmount;
	}

	public int getNewProductAmount() {
		return newProductAmount;
	}

	public void setNewProductAmount(int newProductAmount) {
		this.newProductAmount = newProductAmount;
	}

	public int getActivityAmount() {
		return activityAmount;
	}

	public void setActivityAmount(int activityAmount) {
		this.activityAmount = activityAmount;
	}

	public long getVisitAmount() {
		return visitAmount;
	}

	public void setVisitAmount(long visitAmount) {
		this.visitAmount = visitAmount;
	}

	public int getCollectAmoun() {
		return collectAmoun;
	}

	public void setCollectAmoun(int collectAmoun) {
		this.collectAmoun = collectAmoun;
	}

	public int getTopicAmount() {
		return topicAmount;
	}

	public void setTopicAmount(int topicAmount) {
		this.topicAmount = topicAmount;
	}

}
